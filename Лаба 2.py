def zad2():
    place = int(input("Введите номер вашего места"))
    if place % 2 == 0 and place < 54:
        print ("Верхнее место")
    if place % 2 != 0 and place < 54:
        print ("Нижнее место")
    if place >= 37 and place <= 54:
        print ("Боковое место")
    else:
        print("Купе-место")


