def zad4():
    a = input("Введите название первого цвета")
    b = input("Введите название второго цвета")
    if (a == "красный" and b == "синий") or (a == "синий" and b == "красный"):
        print("Получится фиолетовый!")
    if (a == "красный" and b == "желтый") or (a == "желтый" and b == "красный"):
        print("Получен оранжевый цвет")
    if (a == "синий" and b == "желтый") or (a == "желтый" and b == "синий"):
        print("Получен зелёный цвет")
    else:
        print("Невозможно смешать цвета")
